package com.gobaduchi.common;

/**
 * Enumerator class for type or ranking system (US, China, Japan, Korea, Europe,
 * KGS, IGS, WBADUK, DGS, OGS, FICGS, OTHER, etc)
 * 
 * @author Pete Martin
 */
public final class PlayerRankSystem {

	/**
	 * OTHER_UNKNOWN rank - used when the ranking system is unknown (e.g. cannot
	 * be determined from source of information)
	 */
	public static final PlayerRankSystem OTHER_UNKNOWN = new PlayerRankSystem(
			"Other or unknown");

	/** US (AGA) rank */
	public static final PlayerRankSystem US = new PlayerRankSystem("US");

	/** China (Zhongguo Qiyuan) rank */
	public static final PlayerRankSystem CHINA = new PlayerRankSystem("CHINA");

	/** Japan (Nihon Ki-in) rank */
	public static final PlayerRankSystem JAPAN = new PlayerRankSystem("JAPAN");

	/** Korea (Hankuk Kiwon) rank */
	public static final PlayerRankSystem KOREA = new PlayerRankSystem("KOREA");

	/** Europe (EGF) rank */
	public static final PlayerRankSystem EUROPE = new PlayerRankSystem("EUROPE");

	/** KGS rank */
	public static final PlayerRankSystem KGS = new PlayerRankSystem("KGS");

	/** IGS rank */
	public static final PlayerRankSystem IGS = new PlayerRankSystem("IGS");

	/** OGS rank */
	public static final PlayerRankSystem OGS = new PlayerRankSystem("OGS");

	/** DGS rank */
	public static final PlayerRankSystem DGS = new PlayerRankSystem("DGS");

	/** WBADUK rank */
	public static final PlayerRankSystem WBADUK = new PlayerRankSystem("WBADUK");

	/** FICGS rank */
	public static final PlayerRankSystem FICGS = new PlayerRankSystem("FICGS");

	/** DASHN rank */
	public static final PlayerRankSystem DASHN = new PlayerRankSystem("DASHN");

	/** EWEIQI rank */
	public static final PlayerRankSystem EWEIQI = new PlayerRankSystem("EWEIQI");

	/** TYGEM rank */
	public static final PlayerRankSystem TYGEM = new PlayerRankSystem("TYGEM");

	/** BadukWorld rank */
	public static final PlayerRankSystem BADUKWORLD = new PlayerRankSystem(
			"BADUKWORLD");

	/** Label */
	private String label;

	@Override
	public String toString() {
		return label;
	}

	/**
	 * Private constructor prevents construction outside of this class.
	 */
	private PlayerRankSystem(String label) {
		this.label = label;
	}
}